# ### Ćwiczenie nr 13:
# Wypisz pierwsze 10 liczb naturalnych za pomocą pętli `while`

# Mój komentarz:
# Zgodnie z teorią liczb naturalnych trzeba doprecyzować czy zero uznajemy za liczbą naturalną. 
# Na potrzeby tego zadania wyłączam 0 ze zbioru liczb naturalnych.

x = 1
while x <= 10:
    print(x)
    x += 1